# Program to access values using keys from a dictionary

# Sample dictionary
sample_dict = {
    "name": "Alice",
    "age": 25,
    "city": "New York"
}

# Accessing values using keys
name = sample_dict["name"]
age = sample_dict["age"]
city = sample_dict["city"]

# Printing the values
print(f"Name: {name}")
print(f"Age: {age}")
print(f"City: {city}")