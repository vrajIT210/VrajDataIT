import math

# Using sqrt() to calculate the square root of a number
number = 16
square_root = math.sqrt(number)
print(f"The square root of {number} is {square_root}")

# Using ceil() to round a number up to the nearest integer
num1 = 4.3
rounded_up = math.ceil(num1)
print(f"The ceiling value of {num1} is {rounded_up}")

# Using floor() to round a number down to the nearest integer
num2 = 4.7
rounded_down = math.floor(num2)
print(f"The floor value of {num2} is {rounded_down}")