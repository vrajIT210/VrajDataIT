# Program to separate keys and values from a dictionary

# Sample dictionary
sample_dict = {
    'name': 'Alice',
    'age': 25,
    'city': 'New York'
}

# Separating keys and values
keys = sample_dict.keys()
values = sample_dict.values()

# Displaying keys and values
print("Keys:", list(keys))
print("Values:", list(values))