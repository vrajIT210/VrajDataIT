# Program to update a list using insert() and append()

# Initial list
my_list = [1, 2, 3, 4, 5]

# Using insert() to add an element at a specific position
my_list.insert(2, 99)  # Inserts 99 at index 2
print("List after using insert():", my_list)

# Using append() to add an element at the end of the list
my_list.append(42)  # Appends 42 to the end of the list
print("List after using append():", my_list)