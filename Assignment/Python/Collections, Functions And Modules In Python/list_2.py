# Program to access elements at different index positions in a list

# Sample list
list_1 = [10, 20, 30, 40, 50]

# Accessing elements at different index positions
print("Element at index 0:", list_1[0])  # First element
print("Element at index 2:", list_1[2])  # Third element
print("Element at index -1:", list_1[-1])  # Last element
print("Element at index -3:", list_1[-3])  # Third last element